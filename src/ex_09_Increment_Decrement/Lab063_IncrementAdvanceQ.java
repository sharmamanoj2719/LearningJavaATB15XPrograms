package ex_09_Increment_Decrement;

public class Lab063_IncrementAdvanceQ {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(a++ + a ); // 21
        //  A -> a++ ->  A=10, a = 11
        // +
        // B -> a ->  11
        // A+B
        // 10 + 11

        // ERT
        // LNo. |  a  | Exp (print)
        //  5  | 10 | NA
        //  6 |  11 |  21

    }
}
