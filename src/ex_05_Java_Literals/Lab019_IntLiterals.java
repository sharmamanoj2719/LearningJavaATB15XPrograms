package ex_05_Java_Literals;

public class Lab019_IntLiterals {
    public static void main(String[] args) {
        int age = 65; // 65 is Literals , int is data type, age is variable name , = is operator
/*  Literals are fixed values that you write directly in your code.
    They are the actual data stored in variables (not calculated).
    Examples: 10, 45.6, "Hello", 'A', true, false, null.
    Literals never change unless you rewrite them in code.
    Java has 6 main types of literals: Integer, Floating-point, Character, String, Boolean, Null*/

        System.out.println(age);
    }
}
