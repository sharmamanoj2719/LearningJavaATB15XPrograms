package ex_04_Java_DataType;

public class Lab014_Numerics {
    public static void main(String[] args) {

        byte b = 10;
        short s = 5;
        int i = 20;
        char C = 'S';
        long phone = 9887529366l;
       // must be add l or L in long in last
        float f = 3.84f;
        // must be add f in long in last

        double d = 3034565;
        System.out.println(b);
        System.out.println(s);
        System.out.println(i);
        System.out.println(C);
        System.out.println(phone);

        System.out.println(f);
        System.out.println(d);
    }
}

