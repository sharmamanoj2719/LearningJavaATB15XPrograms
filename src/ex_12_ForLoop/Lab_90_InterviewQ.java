package ex_12_ForLoop;

public class Lab_90_InterviewQ {
    public static void main(String[] args) {

        for (int i = 0; i <= 10; i++) {
            System.out.println(i);
        }

        for (int j = 0; j < 10; j++) {
            System.out.println(j);
        }

        for (int k = 1; k <= 10; k++) {
            System.out.println(k);
        }
    }
}
