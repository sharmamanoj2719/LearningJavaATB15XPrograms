package ex_06_Java_Operators;

public class Lab029_Relational_Operators {

    public static void main(String[] args) {
        // < Less Than
        // < =  -> Less than or equal to
        // > Greater
        // > = Greater or equal
        // == ->  Equal to (but checking)
        // != -> Not equal

        int a = 10;
        int b = 30;
        boolean c = a > b; // // 10> 30
        System.out.println(c);

        int age_Rahul= 33;
        int age_Manoj = 34;

        boolean result = age_Manoj>= age_Rahul;
        System.out.println(result);
    }
    }
